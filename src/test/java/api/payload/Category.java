package api.payload;

public class Category 
{
    private long id;
    private String name;

    // Getter and setter methods for all the fields

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}




