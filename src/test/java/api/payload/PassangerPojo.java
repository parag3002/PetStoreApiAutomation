package api.payload;

public class PassangerPojo 
{

    private String name;
    private int trips;
    private int airline;
    
    
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getTrips() {
		return trips;
	}
	public void setTrips(int trips) {
		this.trips = trips;
	}
	public int getAirline() {
		return airline;
	}
	public void setAirline(int airline) {
		this.airline = airline;
	}
    
    
    
}
